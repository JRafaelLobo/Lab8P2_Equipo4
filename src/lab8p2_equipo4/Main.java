    Dba db = new Dba("./Tabladeuniverso");
    ArrayList<Universo> universoslista = new ArrayList();
    ArrayList<Servivo> servivolista = new ArrayList();
    AdminSerVivos au = new AdminSerVivos("./universosdelprograma.cbm");

    class Fondo extends JPanel {

        public Fondo(String direccion) {
            this.direccion = direccion;
        }

        private String direccion;
        private Image imagen;

        public void paint(Graphics g) {
            imagen = new ImageIcon(direccion).getImage();
            g.drawImage(imagen, 0, 0, getWidth(), getHeight(), this);
            setOpaque(false);
            super.paint(g);
        }
    }

    public void agregar() {
        universoslista.clear();
        try {
            db.query.execute("select Nombre from Universo");
            ResultSet rs = db.query.getResultSet();
            while (rs.next()) {
                Universo uni = new Universo(rs.getString(2));
                universoslista.add(uni);
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }

    public void agregarser() {
        servivolista.clear();
        try {
            db.query.execute("select codigo, Nombre from Universo");
            ResultSet rs = db.query.getResultSet();
            while (rs.next()) {
                Servivo ser = new Servivo(rs.getString(2), rs.getString(3));
                servivolista.add(ser);
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
